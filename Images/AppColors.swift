import UIKit

extension UIColor {
    struct AppColors {
        struct Green {
            static let FadedGreen  = UIColor(red: 0.427, green: 0.788, blue: 0.651, alpha: 1)
        }
        
        struct Purple {
            static let FadedPurp  = UIColor(red: 0.427, green: 0.565, blue: 0.788, alpha: 1)
        }
        
        struct Gray {
            static let BackgroundGray = UIColor(red: 0.259, green: 0.259, blue: 0.259, alpha: 1)
        }
    }
}
